#include "libft.h"

int	check_file_extension(char *s1, char *s2)
{
	while (*s2)
	{
		if (*s2 == '.')
			break ;
		s2++;
	}
	while (*s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
		if (*s1 == '\0' && *s2 == '\0')
			return (1);
	}
	return (0);
}
