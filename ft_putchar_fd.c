#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	unsigned char uni[2];

	if (c >= 0)
	{
		write(fd, &c, 1);
		return ;
	}
	uni[0] = (c >= -64) ? 195 : 194;
	uni[1] = (c >= -64) ? c + 192 : c + 256;
	write(fd, uni, 2);
}
