#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	j;
	size_t	i;

	if (!dst)
		return (0);
	j = ft_strlen(src);
	i = 0;
	if (j == 0)
		dst[j] = src[j];
	while (src[i] && i + 1 < dstsize)
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize > 0)
		dst[i] = '\0';
	return (j);
}
